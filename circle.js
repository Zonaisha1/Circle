let rad = parseInt(prompt("Enter Radius of a circle"));
console.log('Radius = '+rad);

 const pie = 3.14;
 let diameter = 2 * rad;
 let circum = 2 * pie * rad;
 let area = pie * rad * rad;
 console.log('The Diameter of a Circle is : '+diameter+'\nThe Circumference of a Circle is : '+circum+'\nThe Area of a Circle is : '+area);
 